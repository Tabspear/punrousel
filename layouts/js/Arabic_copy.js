  const loadCarouselItems = () => {
    pundit.forEach((item, i) => {
      const carouselItem = document.createElement("div");
      carouselItem.className =
        "inline-block snap-always snap-center ml-4 transition duration-150 hover:scale-50 shadow-2xl shadow-red";
      carouselItem.id = `carousel-item-${i}`;
  
      var img = document.createElement("img");
      img.className = "object-cover object-center w-64 h-64 rounded-lg max-w-xs";
      img.src = `${item.src}`;
      img.alt = `${item.name}`;
  
      carouselItem.appendChild(img);
  
      document.querySelector("#carousel-container").appendChild(carouselItem);
  
      document
        .querySelector(`#carousel-item-${i}`)
        .addEventListener("mouseover", function () {
          showBigCarousel(item)
        });
    });
  };
  
  loadCarouselItems();

  
  
